import java.util.Scanner;

public class SW_problem_1938 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int a, b;	// 계산할 변수 선언
		
		a = sc.nextInt();	// 변수 a의 값을 입력 후 엔터
		b = sc.nextInt();	// 변수 b의 값을 입력 후 엔터
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		
	}
}
